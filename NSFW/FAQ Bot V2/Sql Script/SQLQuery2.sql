# Cortana—Your digital agent
Cortana is ready to help you in more places in Windows 10, including the lock screen. When your device is locked, you can still check your calendar ("What''s my schedule for tomorrow like?") or ask Cortana important questions ("Can you beatbox?").

And forget about writing tasks on your hands just to remember, say, your dad''s birthday—create reminders based on location, time, and even people. Cortana can help you remember to wish your dad a happy birthday at a specific time, or remind you to pick up a cake when you''re at the grocery store.

`Note`
>Cortana is only available in certain countries/regions, and some Cortana features might not be available everywhere. If Cortana isn''t available, you can still use search.

# Rule the web with Microsoft Edge
The context menu for extensions in Microsoft Edge

Microsoft Edge has so many updates, we need a list to cover them all:

* [Use extensions](https://support.microsoft.com/en-us/help/27908) to personalize and protect your browsing with add-ons like Office Online, Microsoft Translator, and more.

* [Enlist Cortana](https://support.microsoft.com/en-us/help/17158) to show you similar image suggestions on Bing searches, or even the lyrics to a music video you''re watching.

* Pin individual tabs to the Start screen for easy access.

* If you have a [touchscreen device](https://support.microsoft.com/en-us/help/17209), browse more briskly by swiping left/right to go back/forward to different webpages.

# Get creative with Windows Ink
[Windows Ink](https://support.microsoft.com/en-us/help/17207) puts the power of Windows in the tip of your tablet pen, enabling you to write on your device just like you do on paper, create sticky notes, draw on a whiteboard, and easily share your analog thoughts in the digital world. Windows Ink is integrated into apps like Maps, OneNote, Microsoft Edge, and Office.

# A new Start
The [Start menu](https://support.microsoft.com/en-us/help/17195) is the place to find all your apps and programs. Personalize your Start screen to show you the apps, tiles, and layout you need to be your most productive self.

Select the Start button, and then Settings  > Personalization  > Start to tinker with your Start screen and make it yours.

# Improved notifications in action center
Action center has moved to a new location in the far right corner of the taskbar, and sports a new look that gives you the power to organize your notifications however you see fit.

You no longer need to open action center to see how many notifications you have—the number on the action center icon  shows you how many notifications are waiting for you. Other apps pinned to the taskbar can also show the number of notifications you''ve missed.

# Sign in to your PC with a Microsoft Band or Windows phone
Unlock your PC with a paired companion device like Microsoft Band or a Windows phone (for example, the Lumia 950 or Lumia 950XL).

# Do more on the lock screen
Cortana isn''t the only new feature on the lock screen—you can now play music while your device is locked to control the action from start to stop. Media controls appear at the bottom right corner of the lock screen, along with a picture of the album art from the song currently playing.',
